package backend_java;

public class EjemploScope {

	public static void main(String[] args) {
		
		int cantidadPersonas = 2;
		
		boolean esPareja;
		if (cantidadPersonas > 1) {
			esPareja = true;
			System.out.println("Es una pareja");
		} else {
			esPareja = false;
			System.out.println("No es pareja");
		}

	}

}
