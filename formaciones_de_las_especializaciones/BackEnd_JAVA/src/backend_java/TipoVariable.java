package backend_java;

public class TipoVariable {
	public static void main(String[] args) {
		System.out.println("Hola Mundo!");
		
		int edad = 28;
		System.out.println(edad);
		String nombre = "David";
		System.out.println(nombre);
		System.out.println(nombre);
		edad = 45;
		System.out.println(edad);
	}

}
